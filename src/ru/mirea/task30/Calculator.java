package ru.mirea.task30;

// Calculator
public class Calculator {
    // Sum
    public int getSum(int a, int b) {
        return a + b;
    }
    // Difference
    public int getDif(int a, int b) {
        return a - b;
    }
    // Product
    public int getMult(int a, int b) {
        return a * b;
    }
    // Quotient
    public double getDiv(int a, int b) {
        return a / b;
    }
    // Square
    public int getSquare(int c) {
        return c * c;
    }
}